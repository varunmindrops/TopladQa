    function getActiveComboPaperFacultyArray(comboId) {
        var facultyPaperArr = [];
        var papersList = $('#' + comboId + ' .paper_lister')

        for (i = 0; i < papersList.length; i++) {
            facultyPaperArr.push({
                facultyId: $(papersList[i]).find('.combo_faculty_id').val(),
