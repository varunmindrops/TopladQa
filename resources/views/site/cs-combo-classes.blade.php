    //to get the cost increase
    function getCostInreaseForFacultyPaperArray(comboCostObj, jsonArrayPaperFaculty) {

        var match = [];
        for (i = 0; i < jsonArrayPaperFaculty.length; i++) {
            if (comboCostObj.increase && comboCostObj.increase.length > 0) {
                var temp = comboCostObj.increase.filter(function(increaseObject) {
                    return (increaseObject['facultyId'] === parseInt(jsonArrayPaperFaculty[i]['facultyId'], 10)) &&
                        (increaseObject['paperNo'] === parseInt(jsonArrayPaperFaculty[i]['paperNo'], 10));
                })[0];
                if (temp) {
                    match.push(temp.costIncrease)
                }
            }
        }
        return match.reduce((a, b) => a + b, 0);
    }

