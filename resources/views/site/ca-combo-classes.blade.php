            var costIncrease = getCostInreaseForFacultyPaperArray(costObj, facultyPaperArray);

            if (sale_mode == "Google Drive") {
                $('#' + selectedComboId + '_hidden_price').val(costObj.discountedPriceGD + costIncrease);
                $('#' + selectedComboId + '_sale_price').text(costObj.discountedPriceGD + costIncrease);
                $('#' + selectedComboId + '_original_price').text(costObj.originalPriceGD + costIncrease);
            }
