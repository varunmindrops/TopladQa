                                 <ol>
                                    <li>‘Skills Training’ is designed to provide students with the targeted training they
                                       need to gain the knowledge and abilities necessary to fulfil the specific
                                       requirements of their job positions. </li>
                                    <li>As a long-term vision of the Institute and professional social responsibility
                                       towards the society and being an organisation under the administrative control of
                                       the Ministry of Corporate Affairs, Government of India, the Institute needs to
                                       ensure that the students pursuing the CMA Course should possess and acquire
                                       robust employability skills to cope with the global challenges and become a
                                       future-ready professional. Hence, the Institute has introduced mandatory SAP
                                       Finance Power User Training & Certification, Microsoft Office Training &
                                       Certification, Cambridge University Press Soft Skill Training & Certification and
                                       E-filing Training & Certification as a bouquet of World Class Employability and
                                       Techno-Skill Training facilities for its Intermediate Students from August 2020
                                       Batch onwards. </li>
                                    <li>‘Skills Training’ and associated assessment tests are mandatory to be eligible
                                       to appear in both/remaining group(s) of CMA Intermediate Examinations.
                                       However, a student can appear in a single group (either first or second group and
                                       not the both/remaining group) of Intermediate Examinations without completing
                                       ‘Skills Training’ and associated assessment tests.
                                       <p>However, Directorate of Studies recommends that the eligible students should
                                          start ‘Skills Training’ activities from the very beginning to their registration in
                                          the Intermediate Course. There is no exemption scheme for ‘Skills Training’ and
                                          it is mandatory for the students taken registration in the Intermediate Course on
                                          or after 11th August 2020.</p>
                                    </li>
                                    <li>
                                       ‘Skills Training’ modules are designed to be accessed as self E-Learning system
