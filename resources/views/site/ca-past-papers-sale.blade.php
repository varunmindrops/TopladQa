$(document).ready(function() {
    $("#jumpTop").hide();
    $(function toTop() {
        $(window).scroll(function() {
            if ($(this).scrollTop() > 100) {
                $('#jumpTop').fadeIn();
            } else {
                $('#jumpTop').fadeOut();
            }
        });

        $('#jumpTop').click(function() {
            $('body,html').animate({
                scrollTop: 0
            }, 600);
            return false;
        });
    });
});

