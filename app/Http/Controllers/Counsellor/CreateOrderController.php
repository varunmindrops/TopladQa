            $request->all(),
            [
                'rzp_order_id' => 'nullable|unique:orders_new',
                'other_payment_id' => 'nullable|unique:orders_new'
        ]);

        if ($validator->fails()) {
            return redirect()->back()->with('error', 'Razorpay Order ID or Other Payment ID Already Exists!');
        } else {

        $data = $request->all();
        return view('admin.super-admin.confirm', compact('data'));
        }
    }

